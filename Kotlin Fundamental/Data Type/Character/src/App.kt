// main function
fun main() {
    var vocal = 'A'

    println("Vocal " + vocal++)   // Vocal A
    println("Vocal " + vocal++)   // Vocal B
    println("Vocal " + vocal++)   // Vocal C
    println("Vocal " + vocal--)   // Vocal D
    println("Vocal " + vocal--)   // Vocal C
    println("Vocal " + vocal--)   // Vocal B
    println("Vocal " + vocal--)   // Vocal A
}